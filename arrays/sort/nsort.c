#include "lib_array.h"

void  nsort(int *arr, size_t n)
{
	int i;

	i = 0;

	while (i < (((int)n) - 1))
		(*(arr + i) != (i + 1)) ? swap(arr + i, (arr + *(arr + i) - 1)) : i++;
}
